// Hair colors per sprite type
const HAIR_COLORS = [
  '#8b4010', // brown (cap)
  '#c0391b', // auburn/red (long)
  '#d4a010', // blonde (spiky)
  '#1a1a2e', // black (headband)
  '#6b3010', // dark brown (bob)
  '#2c1060', // dark purple (afro)
  '#1a50a0', // blue (bun)
]

interface PixelCharacterProps {
  index: number     // determines sprite + hair color
  color: string     // technician primary color → shirt/uniform
  size?: number     // px per pixel, default 3 → 48×48 total
}

export default function PixelCharacter({ index, color, size = 3 }: PixelCharacterProps) {
  const sprite = SPRITES[index % SPRITES.length]
  const hair   = HAIR_COLORS[index % HAIR_COLORS.length]
  const dim    = 16 * size

  function fill(code: number): string {
    switch (code) {
      case 0: return 'transparent'
      case 1: return '#f5c88a'   // skin
      case 2: return hair        // hair
      case 3: return color       // team color (shirt)
      case 4: return '#1e2a4a'   // dark pants
      case 5: return '#111111'   // black outline
      case 6: return '#f5f5f5'   // white
      case 7: return '#2a2a2a'   // dark shoe
      default: return 'transparent'
    }
  }

  return (
    <svg
      width={dim}
      height={dim}
      viewBox={`0 0 ${dim} ${dim}`}
      style={{ imageRendering: 'pixelated', display: 'block' }}
      aria-hidden="true"
    >
      {sprite.flatMap((row, y) =>
        row.map((pixel, x) => {
          if (pixel === 0) return null
          return (
            <rect
              key={`${x}-${y}`}
              x={x * size}
              y={y * size}
              width={size}
              height={size}
              fill={fill(pixel)}
            />
          )
        })
      )}
    </svg>
  )
}
